public class StringDemo {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "Sneha";
        String str3 = str1 + " " + str2;

        System.out.println("String 1: " + str1);
        System.out.println("String 2: " + str2);
        System.out.println("Concatenated String: " + str3);
    }
}
